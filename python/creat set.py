s1 = set()  # إنشاء مجموعة فارغة
s2 = {1, 3, 5}  # إنشاء مجموعة تحتوي على ثلاثة عناصر
s3 = set([1, 3, 5])  # إنشاء مجموعة من قائمة
s4 = set([x * 2 for x in range(1, 10)])  # إنشاء مجموعة من مضاعفات الأعداد من 1 إلى 9
s5 = set("abac")  # إنشاء مجموعة من السلسلة النصية "abac"

# دمج جميع المجموعات في مجموعة واحدة
s6 = s1 | s2 | s3 | s4 | s5

# طباعة المجموعات
print(s1)  # سيظهر: set()
print(s2)  # سيظهر: {1, 3, 5}
print(s3)  # سيظهر: {1, 3, 5}
print(s4)  # سيظهر: {2, 4, 6, 8, 10, 12, 14, 16}
print(s5)  # سيظهر: {'a', 'b', 'c'}

# حساب مجموع العناصر الرقمية فقط
numeric_elements = [x for x in s6 if isinstance(x, int)]
total_sum = sum(numeric_elements)

print(total_sum)  # سيظهر مجموع العناصر الرقمية
